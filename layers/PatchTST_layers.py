def refined_sigmoid(x):
    f = torch.sigmoid(x)
    r = torch.sigmoid(x)
    phi = f * (1 - f) * (2 * r -1)
    g = f + phi
    return g

class HardSigmoidFunction(torch.autograd.Function):
    @staticmethod
    def forward(ctx, input):
        sigmoid_x = torch.sigmoid(input)
        ctx.save_for_backward(sigmoid_x)  # 保存sigmoid_x，以便在反向传播中使用
        
        return torch.where(input > 0, torch.tensor(1), torch.tensor(0))

    @staticmethod
    def backward(ctx, grad_output):
        sigmoid_x, = ctx.saved_tensors  # 从上一步保存的张量中获取sigmoid_x
        grad_input = grad_output.clone()  # clone梯度
        # 计算对sigmoid_x的梯度
        sigmoid_grad = (1.2 * sigmoid_x * (1 - sigmoid_x)) * grad_input
        return sigmoid_grad

def modified_sigmoid(x):
    sigmoid_x = torch.sigmoid(x)
    modified_sigmoid_x = torch.clamp(1.2 * sigmoid_x - 0.1, 0, 1)
    return modified_sigmoid_x

def replace_elements(batch1, batch2):
    bs = batch1.size(0)
    replace_indices = torch.randperm(bs)[:bs//2]  # 随机选择要替换的索引
    new_batch2 = batch2.clone()  # 创建一个副本以保留原始的batch2

    for i in range(replace_indices.size(0)):
        index = replace_indices[i]
        new_batch2[index] = batch1[index]

    return new_batch2

def gating(gate): 
    if gate.requires_grad:
        noise = torch.randn_like(gate)
        gate_with_noise = gate + noise
    else:
        gate_with_noise = gate
    g1 = modified_sigmoid(gate_with_noise)
    g2 = HardSigmoidFunction.apply(gate_with_noise)

    if gate.requires_grad:
        g = replace_elements(g1, g2)
        return g
    else:
        return g2